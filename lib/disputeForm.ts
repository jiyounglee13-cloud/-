import type { AppealInput } from "./types";

/**
 * 금융감독원 금융분쟁조정 신청서 양식 생성기.
 *
 * 사용자 입력(AppealInput)과 생성된 이의신청서 본문을 바탕으로, 금융감독원에
 * 제출하는 '금융분쟁조정 신청서' 서식의 빈칸을 채워 초안을 만든다. 인적사항 등
 * 민감 정보는 사용자가 직접 기입하도록 플레이스홀더로 둔다.
 *
 * (연구서 기조) 본 도구는 사전 정의된 서식의 빈칸을 채우는 보조 역할만 하며,
 * 독자적 법률 판단을 하지 않는다.
 */

const CATEGORY_LABEL: Record<string, string> = {
  cataract: "백내장 수술(다초점렌즈) 입원 의료비",
  manual_therapy: "도수치료 비용",
  knee_stemcell: "무릎 줄기세포 시술 입원 의료비",
  medical_advisory: "보험사 의료자문 결과에 따른 부지급",
  general: "실손의료보험금",
};

export function buildDisputeMediationForm(
  input: AppealInput,
  appealBody: string,
): string {
  const subject = CATEGORY_LABEL[input.category] || "실손의료보험금";
  const today = new Date().toISOString().slice(0, 10);

  return `금융분쟁조정 신청서

■ 신청인 (소비자)
  - 성명: __________________ (직접 기입)
  - 연락처: __________________ (직접 기입)
  - 주소: __________________ (직접 기입)

■ 피신청인 (금융회사)
  - 회사명: __________________ (보험회사명 기입)
  - 관련 상품: 실손의료보험

■ 신청 취지
  피신청인이 ${subject}에 대하여 행한 보험금 부지급(면책) 결정은 부당하므로,
  정당한 보험금의 지급을 구하기 위하여 분쟁조정을 신청합니다.

■ 분쟁 개요
  - 진단명: ${input.diagnosis || "(기입)"}
  - 진료(시술)일: ${input.treatmentDate || "(기입)"}
  - 청구 금액: ${input.claimAmount || "(기입)"}원
  - 피신청인 부지급 사유: ${input.rejectionReason || "(기입)"}

■ 신청 이유 (아래는 이의신청서 본문을 토대로 자동 구성된 초안입니다)
${appealBody.trim()}

■ 첨부 서류 (해당 시 첨부)
  □ 보험금 부지급(면책) 통지서 사본
  □ 진단서 / 소견서
  □ 진료비 영수증 및 세부산정내역서
  □ 의무기록 사본 (입원기록, 활력징후 등)
  □ 기타 입증 자료

신청일: ${today}
신청인: __________________ (서명)

──────────────────────────────────────────────
본 문서는 사용자의 입력을 바탕으로 표준 서식의 빈칸을 채워 생성된 초안일 뿐,
법률·의학 자문이 아닙니다. 제출 전 인적사항과 사실관계를 직접 확인하고,
금융감독원(국번없이 1332) 또는 금융소비자 보호 절차를 통해 접수하시기 바랍니다.`;
}
